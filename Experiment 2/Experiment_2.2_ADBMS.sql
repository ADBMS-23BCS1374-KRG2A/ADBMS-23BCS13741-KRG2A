SELECT 
 Q.*, 
 ISNULL(M.SuccessPoints, 0) AS SuccessPoints
FROM
 MISSIONS_TBL AS M
RIGHT OUTER JOIN
 MISSION_QUERIES_TBL AS Q
ON 
 M.ShinobiID = Q.ShinobiID 
 AND
 M.Year = Q.Year;
